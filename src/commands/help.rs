use super::registry::{command_group_name, is_running_safe_command_name, CommandHelpSummary};
use anyhow::{bail, Result};

pub(super) fn help_text() -> String {
    let mut lines = help_topics()
        .iter()
        .map(|topic| topic.listing)
        .collect::<Vec<_>>();
    lines.push("");
    lines.push("Run `/help <command>` for details, or `/help all` for the full guide.");
    lines.join("\n")
}

pub(super) fn help_for(args: &[String]) -> Result<String> {
    match args {
        [] => Ok(help_text()),
        [topic] if topic == "all" => Ok(help_topics()
            .iter()
            .map(format_help_topic)
            .collect::<Vec<_>>()
            .join("\n\n")),
        [topic] => {
            let normalized = normalize_help_topic(topic);
            help_topics()
                .iter()
                .find(|item| item.name == normalized)
                .map(format_help_topic)
                .ok_or_else(|| {
                    anyhow::anyhow!("unknown help topic `{topic}`; run `/help` to list commands")
                })
        }
        _ => bail!("usage: /help [command|all]"),
    }
}

pub(super) fn help_summaries() -> Vec<CommandHelpSummary> {
    help_topics()
        .iter()
        .map(|topic| CommandHelpSummary {
            name: topic.name,
            listing: topic.listing,
            summary: topic.summary,
            usage: topic.usage,
            examples: topic.examples,
            notes: topic.notes,
            running_safe: is_running_safe_command_name(topic.name),
            group: command_group_name(topic.name),
        })
        .collect()
}

pub(super) fn command_names() -> Vec<&'static str> {
    help_topics().iter().map(|topic| topic.name).collect()
}

